    private void editBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBTActionPerformed
         int row = peminjamanTB.getSelectedRow();

    if(row >= 0){

        DefaultTableModel model =
                (DefaultTableModel) peminjamanTB.getModel();

        model.setValueAt(idpeminjamanTF.getText(), row, 1);
        model.setValueAt(peminjamCMB.getSelectedItem(), row, 2);
        model.setValueAt(namapeminjamTF.getText(), row, 3);
        model.setValueAt(idbukuCMB.getSelectedItem(), row, 4);
        model.setValueAt(judulbukuTF.getText(), row, 5);
        model.setValueAt(tglpinjamTF.getText(), row, 6);
        model.setValueAt(tglkembaliTF.getText(), row, 7);
    }
    }//GEN-LAST:event_editBTActionPerformed

